(ns day-10.day10-test
  (:require [day-10.day10 :as sut]
            [clojure.test :as t]))


(t/deftest segments
  (t/is (= (sut/segments '(0 1 4 5 6 7 10 11 12 15 16 19 22))
           '((0 1) (4 5 6 7) (10 11 12) (15 16) (19) (22))))

  (t/is (= (sut/segments '(0 1 4 5 6 7 10 11 12 15 16 19))
           '((0 1) (4 5 6 7) (10 11 12) (15 16) (19))))

  (t/is (= (sut/segments '(0 1 2 3 4 7 8 9 10 11 14 17 18 19 20 23 24 25 28 31 32 33 34 35 38 39 42 45 46 47 48 49 52))
           '((0 1 2 3 4) (7 8 9 10 11) (14) (17 18 19 20) (23 24 25) (28) (31 32 33 34 35)
             (38 39) (42) (45 46 47 48 49) (52))))

  (t/is (= (sut/segments '(1 2 10 11 100 101))
           '((1 2) (10 11) (100 101))))

  (t/is (= (sut/segments '(1 2 3   7 8 9   11))
           '((1 2 3) (7 8 9) (11))))
  )
